public class WatchModell {

    private String Name;
    private String WatchAndroidVersion;
    private String WatchIosVersion;
    private String WatchWinVersion;
    private String WatchIPcert;
    private String WatchIPother;
    private String WatchDesc;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getWatchAndroidVersion() {
        return WatchAndroidVersion;
    }

    public void setWatchAndroidVersion(String watchAndroidVersion) {
        WatchAndroidVersion = watchAndroidVersion;
    }

    public String getWatchIosVersion() {
        return WatchIosVersion;
    }

    public void setWatchIosVersion(String watchIosVersion) {
        WatchIosVersion = watchIosVersion;
    }

    public String getWatchWinVersion() {
        return WatchWinVersion;
    }

    public void setWatchWinVersion(String watchWinVersion) {
        WatchWinVersion = watchWinVersion;
    }

    public String getWatchIPcert() {
        return WatchIPcert;
    }

    public void setWatchIPcert(String watchIPcert) {
        WatchIPcert = watchIPcert;
    }

    public String getWatchIPother() {
        return WatchIPother;
    }

    public void setWatchIPother(String watchIPother) {
        WatchIPother = watchIPother;
    }

    public String getWatchDesc() {
        return WatchDesc;
    }

    public void setWatchDesc(String watchDesc) {
        WatchDesc = watchDesc;
    }
}
